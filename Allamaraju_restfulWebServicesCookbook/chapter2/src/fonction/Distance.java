package fonction;

public class Distance {
	private float lats, lngs, late, lnge;

	public float getLats() {
		return lats;
	}

	public void setLats(float lats) {
		this.lats = lats;
	}

	public float getLngs() {
		return lngs;
	}

	public void setLngs(float lngs) {
		this.lngs = lngs;
	}

	public float getLate() {
		return late;
	}

	public void setLate(float late) {
		this.late = late;
	}

	public float getLnge() {
		return lnge;
	}

	public void setLnge(float lnge) {
		this.lnge = lnge;
	}
	

}
