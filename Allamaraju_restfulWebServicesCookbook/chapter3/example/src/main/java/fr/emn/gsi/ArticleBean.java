package fr.emn.gsi;


public class ArticleBean {
    private Integer id;
    private String name;
    private String href;

    public String getHref() {
        return href;
    }

    public void setHref(String href) {
        this.href = href;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer val1) {
        this.id = val1;
    }

    public String getName() {
        return name;
    }

    public void setName(String val2) {
        this.name = val2;
    }

}
